import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        int m = sc.nextInt();
        // Please write your code here.
        int minNum = n>m ? m:n;
        while(minNum!=0){
            if(n%minNum==0 && m%minNum==0){
                System.out.println(minNum*n/minNum*m/minNum);
                break;
            }
            minNum--;
        }
    }
}